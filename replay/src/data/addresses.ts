// Address with upper 96 bits non-zero indicates a packed inline value rather
// than a real EVM contract address. Inline moves carry their own metadata.
export function isInlineAddress(addr: string): boolean {
  return BigInt(addr) >> 160n !== 0n;
}

// Snake-case key -> PascalCase contract name. Matches factories.ts naming.
export function snakeToPascal(key: string): string {
  return key.toLowerCase().split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('');
}
